# 1. PDF READING MODULE (PyMuPDF)
# ---------------------------------------------------------------------------
def extract_pdf_text(pdf_path: str) -> str:
    """
    Extracts all text from a PDF file using PyMuPDF.

    Args:
        pdf_path: Path to the PDF file.

    Returns:
        Full text extracted from the PDF.

    Raises:
        FileNotFoundError: If the file does not exist.
        RuntimeError: If the PDF cannot be read.
    """
    path = Path(pdf_path)
    if not path.exists():
        raise FileNotFoundError(f"File not found: {pdf_path}")

    try:
        doc = fitz.open(str(path))
        pages_text = []
        total_pages = len(doc)
        for page_num, page in enumerate(doc, start=1):
            text = page.get_text("text")
            if text.strip():
                pages_text.append(text.strip())
                log.info(f"  Page {page_num}: {len(text)} characters extracted")
        doc.close()

        result = "\n\n".join(pages_text)
        if not result.strip():
            raise RuntimeError("The PDF contains no extractable text.")

        log.info(f"  Total: {len(result)} characters from {total_pages} pages")
        return result

    except Exception as e:
        raise RuntimeError(f"Error reading PDF: {e}")


