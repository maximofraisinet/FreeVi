# Chunk-based script generation helpers
# ---------------------------------------------------------------------------

# Keywords that identify thinking/reasoning models (case-insensitive substring
# match against the model name).
_THINKING_KEYWORDS = ("thinking", "think", "qwq", "r1", "reasoner")

# Tokens consumed by the internal reasoning chain of thinking models before
# they write the actual JSON output.
_THINKING_OVERHEAD = 2048

# Tokens reserved for the system prompt and user message wrappers.
_SYSTEM_PROMPT_TOKENS = 400

# Tokens reserved per scene for JSON output (narrator_text + video_query +
# JSON scaffolding).  800 tokens is sufficient for verbose models like
# gemma3 whose narrator_text can easily reach 600–700 tokens per scene.
_TOKENS_PER_SCENE = 800

# Minimum JSON output budget regardless of scene count.
_OUTPUT_BASE_TOKENS = 512

# Characters per token estimate (conservative, works for mixed-language text).
_CHARS_PER_TOKEN = 3


def _is_thinking_model(model: str) -> bool:
    """Returns True if the model name suggests it uses a reasoning/thinking chain."""
    return any(kw in model.lower() for kw in _THINKING_KEYWORDS)


def _next_power_of_two(n: int) -> int:
    """Returns the smallest power of two that is >= n."""
    p = 1
    while p < n:
        p <<= 1
    return p


def _calculate_context_size(chunk_tokens: int, scenes_per_chunk: int,
                             thinking: bool) -> int:
    """
    Derives the Ollama num_ctx that comfortably fits one chunk call.

    Formula:
        raw = chunk_tokens + output_budget + system_tokens + thinking_overhead
        context_size = next power of two >= raw

    The result is rounded up to a power of two so Ollama uses a clean KV-cache
    size, which also gives a small extra safety margin.

    Args:
        chunk_tokens: Number of input tokens for the PDF chunk.
        scenes_per_chunk: How many scenes will be requested in this call.
        thinking: Whether the model uses an internal reasoning chain.

    Returns:
        Recommended num_ctx value.
    """
    output_budget = scenes_per_chunk * _TOKENS_PER_SCENE + _OUTPUT_BASE_TOKENS
    thinking_extra = _THINKING_OVERHEAD if thinking else 0
    raw = chunk_tokens + output_budget + _SYSTEM_PROMPT_TOKENS + thinking_extra
    return _next_power_of_two(raw)


def _split_into_chunks(text: str, max_chars: int) -> list[str]:
    """
    Splits *text* into chunks of at most *max_chars* characters each.

    Prefers to cut at paragraph boundaries (double newline).  Falls back to
    sentence boundaries ('. ') if no paragraph break is found.  Hard-cuts at
    *max_chars* as a last resort so no chunk ever exceeds the limit.

    Args:
        text: The full text to split.
        max_chars: Maximum characters per chunk.

    Returns:
        List of non-empty text chunks.
    """
    if len(text) <= max_chars:
        return [text]

    chunks: list[str] = []
    remaining = text
    while remaining:
        if len(remaining) <= max_chars:
            chunks.append(remaining)
            break

        # Try to cut at the last paragraph break within the window
        window = remaining[:max_chars]
        cut = window.rfind("\n\n")
        if cut == -1:
            # Fall back to last sentence boundary
            cut = window.rfind(". ")
            if cut != -1:
                cut += 1  # include the period
        if cut == -1:
            # Hard cut
            cut = max_chars

        chunks.append(remaining[:cut].rstrip())
        remaining = remaining[cut:].lstrip()

    return [c for c in chunks if c]


def count_chunks(pdf_text: str, chunk_tokens: int) -> int:
    """
    Returns the number of chunks that *pdf_text* would be split into for a
    given *chunk_tokens* setting.

    This is a lightweight preview function intended for the GUI so it can
    update the max-scenes spinner in real time without running the full
    pipeline.

    Args:
        pdf_text: Full text extracted from the PDF.
        chunk_tokens: Chunk size in tokens (same as the GUI's chunk_size field).

    Returns:
        Number of chunks (always >= 1).
    """
    max_chars = max(500, chunk_tokens * _CHARS_PER_TOKEN)
    return len(_split_into_chunks(pdf_text, max_chars))


def _summarize_scenes(scenes: list) -> str:
    """
    Builds a short plain-text summary of the most recently generated scenes
    WITHOUT making an extra LLM call.  Used to give the model context about
    what has already been narrated so it does not repeat itself.

    Takes up to the last 3 scenes and keeps the first 80 characters of each
    narrator_text.

    Args:
        scenes: List of Scene objects generated so far.

    Returns:
        A one-line summary string, or empty string if no scenes yet.
    """
    if not scenes:
        return ""
    recent = scenes[-3:]
    parts = [s.narrator_text[:80].rstrip().rstrip(".") + "…" for s in recent]
    return " | ".join(parts)


def _parse_scenes_from_json(
    content: str, offset: int = 0, max_scenes: int = 0
) -> list:
    """
    Parses a JSON string (possibly dirty) into a list of raw scene dicts.
    Returns an empty list if parsing fails.

    Args:
        content: Raw LLM output string.
        offset: Scene number to start from (for correct renumbering).
        max_scenes: If > 0, truncate the result to this many scenes.

    Returns:
        List of Scene objects.
    """
    cleaned = _clean_json(content)
    # Try to fix typographic quotes
    for src, dst in [("\u201c", '"'), ("\u201d", '"'),
                     ("\u2018", "'"), ("\u2019", "'")]:
        cleaned = cleaned.replace(src, dst)

    try:
        data = json.loads(cleaned)
    except json.JSONDecodeError:
        return []

    scenes_list = None
    for key in ("scenes", "escenas", "script", "guion", "video_scenes", "scene"):
        val = data.get(key) if isinstance(data, dict) else None
        if isinstance(val, list) and val:
            scenes_list = val
            break
    if scenes_list is None and isinstance(data, list):
        scenes_list = data
    if not scenes_list:
        return []

    result = []
    for i, raw in enumerate(scenes_list):
        if not isinstance(raw, dict):
            continue
        narrator_text = (
            raw.get("narrator_text") or raw.get("texto_narrador")
            or raw.get("narrator") or raw.get("narrador")
            or raw.get("text") or ""
        ).strip()
        video_query = (
            raw.get("video_query") or raw.get("query")
            or raw.get("visual") or raw.get("video") or ""
        ).strip()
        if not narrator_text:
            continue
        if not video_query:
            video_query = "nature landscape"
        video_query = " ".join(video_query.split()[:5])
        result.append(
            Scene(number=offset + len(result) + 1,
                  narrator_text=narrator_text,
                  video_query=video_query)
        )
    if max_scenes > 0:
        result = result[:max_scenes]
    return result


def _call_ollama(model: str, system_prompt: str, user_prompt: str,
                 context_size: int, num_predict: int) -> tuple[str, str]:
    """
    Makes a single ollama.chat call and returns (content, done_reason).

    Handles the thinking-model edge case where content is empty but the
    answer is embedded inside message.thinking.

    Returns:
        (content_str, done_reason_str)
    """
    try:
        response = ollama.chat(
            model=model,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user",   "content": user_prompt},
            ],
            format="json",
            options={"num_ctx": context_size, "num_predict": num_predict},
        )
    except Exception as e:
        raise ConnectionError(
            f"Error connecting to Ollama. Is the server running?\n"
            f"Run: ollama serve\nDetails: {e}"
        )

    content = response["message"]["content"]

    # Thinking models may leave content empty if reasoning consumed all tokens.
    if not content.strip():
        thinking = response["message"].get("thinking", "") or ""
        if thinking:
            log.warning("  content is empty; scanning thinking field for JSON...")
            blocks = re.findall(r'\{[\s\S]*\}', thinking)
            if blocks:
                content = blocks[-1]
                log.info(f"  Found JSON in thinking field ({len(content)} chars).")

    done_reason = (
        response.get("done_reason", "") or response.get("stop_reason", "")
    )
    return content, done_reason


def generate_script(
    pdf_text: str,
    model: str = "qwen3",
    max_scenes: int = 8,
    custom_instructions: str = "",
    chunk_size: int = 4096,
    narration_language: str = "Spanish",
    on_progress=None,
) -> Script:
    """
    Converts PDF text into a narrated video script by calling a local LLM
    (via Ollama) one chunk at a time.

    Chunk-based strategy
    --------------------
    Instead of truncating the PDF to fit a single context window, the text is
    split into chunks whose size is controlled by *chunk_size* (in tokens).
    For each chunk the function derives the required Ollama ``num_ctx``
    automatically:

        num_ctx = next_power_of_two(
            chunk_size + output_budget + system_prompt + thinking_overhead
        )

    This means the user only needs to set how many tokens of PDF content they
    want per call; the context window is calculated for them.

    Thinking models (names containing "thinking", "think", "qwq", "r1",
    "reasoner") receive an extra 2048-token overhead in both ``num_predict``
    and the context window so the reasoning chain does not crowd out the JSON.

    Args:
        pdf_text: Text extracted from the PDF.
        model: Ollama model name.
        max_scenes: Total number of scenes to generate across all chunks.
        custom_instructions: Free-form instructions injected into the system
            prompt.  If empty, DEFAULT_CUSTOM_INSTRUCTIONS is used.
        chunk_size: Number of PDF-text tokens per LLM call.  The actual
            context window is derived from this value automatically.
            Default: 4096.
        narration_language: Language label forced into the system prompt (e.g.
            "Spanish", "English (American)").
        on_progress: Optional callable(chunk_idx: int, total_chunks: int)
            called before each chunk call so the GUI can update its progress
            bar.

    Returns:
        Script with all generated scenes renumbered from 1.

    Raises:
        ConnectionError: If Ollama is not reachable.
        ValueError: If no valid scenes could be extracted from any chunk.
    """
    thinking = _is_thinking_model(model)

    # ── Step 1: determine chunk size in characters and split ──────────────────
    # Conservative estimate: 1 token ≈ 3 characters.
    max_chars_per_chunk = max(500, chunk_size * _CHARS_PER_TOKEN)
    chunks = _split_into_chunks(pdf_text, max_chars_per_chunk)
    n_chunks = len(chunks)

    log.info(
        f"  PDF split into {n_chunks} chunk(s) "
        f"(chunk_size={chunk_size} tokens, "
        f"~{max_chars_per_chunk} chars/chunk)"
    )

    # ── Step 2: distribute scenes across chunks ───────────────────────────────
    # Rules:
    #   • Minimum total scenes = n_chunks (at least 1 per chunk).
    #   • Total scenes must be a multiple of n_chunks so every chunk gets
    #     exactly the same number of scenes (no remainder, no last-chunk spike).
    #   • If the user's max_scenes is below the minimum or is not a multiple,
    #     round UP to the next valid multiple and log a warning.
    min_scenes      = n_chunks                                # 1 scene / chunk
    # Round max_scenes up to the nearest multiple of n_chunks
    adjusted        = max(max_scenes, min_scenes)
    remainder_adj   = adjusted % n_chunks
    if remainder_adj != 0:
        adjusted += n_chunks - remainder_adj
    if adjusted != max_scenes:
        log.warning(
            f"  max_scenes adjusted from {max_scenes} → {adjusted} "
            f"(must be a non-zero multiple of {n_chunks} chunks; "
            f"valid values: {', '.join(str(n_chunks * k) for k in range(1, 6))}…)"
        )
    spc = adjusted // n_chunks              # scenes per chunk — identical for all
    scenes_per_chunk = [spc] * n_chunks

    # ── Step 3: derive num_ctx for the heaviest chunk ─────────────────────────
    context_size = _calculate_context_size(chunk_size, spc, thinking)
    num_predict  = spc * _TOKENS_PER_SCENE + _OUTPUT_BASE_TOKENS
    if thinking:
        num_predict += _THINKING_OVERHEAD

    log.info(
        f"  Scenes: {adjusted} total ({spc}/chunk × {n_chunks} chunks)  "
        f"| context_size (num_ctx) = {context_size} tokens  "
        f"[chunk={chunk_size} + output={num_predict} + "
        f"sys={_SYSTEM_PROMPT_TOKENS}"
        + (f" + thinking={_THINKING_OVERHEAD}" if thinking else "")
        + f", rounded to next power of 2]"
    )
    if thinking:
        log.info(f"  Thinking model detected ('{model}'). "
                 f"Adding {_THINKING_OVERHEAD} token overhead for reasoning.")

    # ── Step 4: process each chunk ────────────────────────────────────────────
    all_scenes: list[Scene] = []
    previous_summary = ""

    for chunk_idx, (chunk_text, spc) in enumerate(
            zip(chunks, scenes_per_chunk), start=1):

        if on_progress:
            on_progress(chunk_idx, n_chunks)

        log.info(
            f"  ── Chunk {chunk_idx}/{n_chunks}: "
            f"{len(chunk_text)} chars, {spc} scene(s) requested ──"
        )

        scenes = _generate_chunk_attempt(
            chunk_text=chunk_text,
            chunk_idx=chunk_idx,
            total_chunks=n_chunks,
            scenes_for_chunk=spc,
            previous_summary=previous_summary,
            model=model,
            context_size=context_size,
            num_predict=num_predict,
            custom_instructions=custom_instructions,
            narration_language=narration_language,
            scene_offset=len(all_scenes),
            attempt=1,
        )

        if scenes:
            all_scenes.extend(scenes)
            previous_summary = _summarize_scenes(all_scenes)
            log.info(f"  Chunk {chunk_idx}/{n_chunks}: {len(scenes)} scene(s) added "
                     f"(total so far: {len(all_scenes)})")
        else:
            log.warning(f"  Chunk {chunk_idx}/{n_chunks}: no valid scenes extracted, skipping.")

    if not all_scenes:
        raise ValueError("The LLM did not generate any valid scenes across all chunks.")

    # Renumber scenes 1..N
    for idx, scene in enumerate(all_scenes, start=1):
        scene.number = idx

    log.info(f"  Script complete: {len(all_scenes)} scenes across {n_chunks} chunk(s).")
    script = Script()
    script.scenes = all_scenes
    script.adjusted_max_scenes = adjusted
    script.n_chunks = n_chunks
    return script


def _generate_chunk_attempt(
    chunk_text: str,
    chunk_idx: int,
    total_chunks: int,
    scenes_for_chunk: int,
    previous_summary: str,
    model: str,
    context_size: int,
    num_predict: int,
    custom_instructions: str,
    narration_language: str,
    scene_offset: int,
    attempt: int,
) -> list:
    """
    Single attempt to generate scenes for one PDF chunk.

    Retries up to 3 times with progressively fewer scenes if the response is
    truncated or contains invalid JSON.

    Args:
        chunk_text: The PDF text for this chunk.
        chunk_idx: 1-based index of this chunk.
        total_chunks: Total number of chunks.
        scenes_for_chunk: How many scenes to request.
        previous_summary: Short summary of already-narrated scenes (may be "").
        model: Ollama model name.
        context_size: num_ctx to pass to Ollama.
        num_predict: num_predict to pass to Ollama.
        custom_instructions: Free-form instructions.
        narration_language: Language label for the system prompt.
        scene_offset: Number of scenes already generated (for numbering).
        attempt: Current attempt number (1-3).

    Returns:
        List of Scene objects (may be empty if all retries failed).
    """
    # Build a chunk-aware system prompt
    part_header = f"Part {chunk_idx} of {total_chunks} of the document."
    if chunk_idx == total_chunks and total_chunks > 1:
        part_header += " This is the FINAL part — write a conclusion scene."
    context_note = ""
    if previous_summary:
        context_note = (
            f"\nALREADY NARRATED (do NOT repeat these topics):\n{previous_summary}\n"
            f"Continue naturally from where the narration left off."
        )

    system_prompt = build_system_prompt(
        scenes_for_chunk, custom_instructions, narration_language,
        chunk_header=part_header + context_note,
    )
    user_prompt = (
        f"Convert the following text excerpt into a narrated video script "
        f"({scenes_for_chunk} scene(s) for this part):\n\n"
        f"---START OF EXCERPT---\n{chunk_text}\n---END OF EXCERPT---"
    )

    log.info(
        f"    context_size={context_size}, num_predict={num_predict}, "
        f"scenes={scenes_for_chunk}, attempt={attempt}/3"
    )

    content, done_reason = _call_ollama(
        model, system_prompt, user_prompt, context_size, num_predict,
    )
    log.info(f"    LLM response: {len(content)} chars, done_reason={done_reason!r}")

    # If truncated and retries remain, reduce scenes and double num_predict
    if done_reason == "length" and attempt < 3:
        reduced = max(1, scenes_for_chunk // 2) if attempt == 1 else 1
        log.warning(
            f"    Truncated (done_reason=length). "
            f"Retrying chunk {chunk_idx} with {reduced} scene(s) "
            f"and num_predict={num_predict * 2} "
            f"(attempt {attempt + 1}/3)..."
        )
        return _generate_chunk_attempt(
            chunk_text, chunk_idx, total_chunks, reduced, previous_summary,
            model, context_size, num_predict * 2, custom_instructions,
            narration_language, scene_offset, attempt + 1,
        )

    scenes = _parse_scenes_from_json(
        content, offset=scene_offset, max_scenes=scenes_for_chunk
    )

    # If JSON was invalid and retries remain, reduce scenes and try again
    if not scenes and attempt < 3:
        reduced = max(1, scenes_for_chunk // 2) if attempt == 1 else 1
        log.warning(
            f"    No valid scenes from JSON. "
            f"Retrying chunk {chunk_idx} with {reduced} scene(s) "
            f"(attempt {attempt + 1}/3)..."
        )
        return _generate_chunk_attempt(
            chunk_text, chunk_idx, total_chunks, reduced, previous_summary,
            model, context_size, num_predict, custom_instructions,
            narration_language, scene_offset, attempt + 1,
        )

    return scenes


