class AudioEngine:
    """
    Generates TTS audio using Kokoro 1.0 with ONNX Runtime.

    Supports all Kokoro languages.  For espeak-backed languages (American
    English, British English, Spanish, French, Hindi, Italian, Portuguese)
    it uses ``misaki.espeak.EspeakG2P`` for phonemization.  For Japanese
    and Mandarin Chinese it falls back to Kokoro's built-in G2P.
    """

    def __init__(
        self,
        voice: str = "af_heart",
        speed: float = 1.0,
        lang_code: str = "",
    ):
        """
        Initializes the audio engine.

        Args:
            voice: Kokoro voice ID (e.g. ``af_heart``, ``em_alex``).
            speed: Speech speed (1.0 = normal).
            lang_code: Kokoro single-char language code (``a``, ``e``, …).
                       If empty, inferred from the voice prefix.
        """
        self.voice = voice
        self.speed = speed
        self.lang_code = lang_code or get_lang_code_for_voice(voice)
        self.kokoro = None
        self.g2p = None

    def _initialize(self):
        """Loads the Kokoro model and the appropriate G2P module (lazy)."""
        if self.kokoro is not None:
            return

        # Verify model files exist
        if not KOKORO_ONNX_PATH.exists():
            raise FileNotFoundError(
                f"Kokoro model not found at: {KOKORO_ONNX_PATH}\n"
                f"Download it from: https://github.com/thewh1teagle/kokoro-onnx/releases"
            )
        if not KOKORO_VOICES_PATH.exists():
            raise FileNotFoundError(
                f"Kokoro voices not found at: {KOKORO_VOICES_PATH}\n"
                f"Download them from: https://github.com/thewh1teagle/kokoro-onnx/releases"
            )

        log.info("  Loading Kokoro v1.0 model (this may take a few seconds)...")
        self.kokoro = Kokoro(str(KOKORO_ONNX_PATH), str(KOKORO_VOICES_PATH))

        # Initialize G2P based on language
        lang_info = KOKORO_LANGUAGES.get(self.lang_code, {})
        espeak_code = lang_info.get("espeak_code")
        lang_label = lang_info.get("label", self.lang_code)

        if espeak_code is not None:
            # espeak-backed language — use misaki EspeakG2P
            try:
                from misaki.espeak import EspeakG2P
                self.g2p = EspeakG2P(language=espeak_code)
                log.info(f"  G2P initialized for {lang_label} (espeak: {espeak_code})")
            except ImportError:
                log.warning(
                    f"  misaki not available. Using direct mode for {lang_label}.\n"
                    f"  For better quality: pip install misaki"
                )
                self.g2p = None
        else:
            # Japanese / Chinese — Kokoro handles G2P internally
            log.info(f"  {lang_label}: using Kokoro built-in G2P (no espeak needed)")
            self.g2p = None

    @staticmethod
    def _clean_tts_text(text: str) -> str:
        """
        Removes characters that TTS engines read aloud literally:
          - Emoji (Unicode ranges for emoticons, symbols, pictographs, etc.)
          - Asterisks used as markdown emphasis markers  (* and **)
          - Isolated hash characters used as markdown headings (# at line start)
          - Leading/trailing whitespace after cleaning.
        """
        # Strip markdown emphasis: **bold**, *italic*, ***both***
        text = re.sub(r'\*+', '', text)
        # Strip markdown headings: ## Title → Title
        text = re.sub(r'^\s*#+\s*', '', text, flags=re.MULTILINE)
        # Strip emoji — covers the main Unicode emoji blocks:
        #   U+1F600-1F64F  Emoticons
        #   U+1F300-1F5FF  Misc symbols & pictographs
        #   U+1F680-1F6FF  Transport & map
        #   U+1F700-1F77F  Alchemical symbols
        #   U+1F780-1F7FF  Geometric shapes extended
        #   U+1F800-1F8FF  Supplemental arrows-C
        #   U+1F900-1F9FF  Supplemental symbols & pictographs
        #   U+1FA00-1FA6F  Chess / symbols
        #   U+1FA70-1FAFF  Symbols & pictographs extended-A
        #   U+2600-26FF    Misc symbols (☀ ☁ ♥ etc.)
        #   U+2700-27BF    Dingbats
        #   U+FE00-FE0F    Variation selectors (emoji modifiers)
        #   U+1F1E0-1F1FF  Regional indicator letters (flag emoji)
        text = re.sub(
            r'[\U0001F600-\U0001F64F'
            r'\U0001F300-\U0001F5FF'
            r'\U0001F680-\U0001F6FF'
            r'\U0001F700-\U0001F77F'
            r'\U0001F780-\U0001F7FF'
            r'\U0001F800-\U0001F8FF'
            r'\U0001F900-\U0001F9FF'
            r'\U0001FA00-\U0001FA6F'
            r'\U0001FA70-\U0001FAFF'
            r'\U00002600-\U000026FF'
            r'\U00002700-\U000027BF'
            r'\U0000FE00-\U0000FE0F'
            r'\U0001F1E0-\U0001F1FF'
            r']',
            '',
            text,
        )
        # Collapse multiple spaces left by removals
        text = re.sub(r'  +', ' ', text)
        return text.strip()

    def generate_audio(self, text: str, output_path: str) -> float:
        """
        Generates a .wav file from text in the configured language.

        Args:
            text: Text to synthesize (in the voice's language).
            output_path: Path where the .wav file will be saved.

        Returns:
            Exact audio duration in seconds.
        """
        self._initialize()
        text = self._clean_tts_text(text)

        try:
            if self.g2p is not None:
                # Convert text to phonemes via misaki espeak
                # misaki 0.7+ EspeakG2P returns str directly
                phonemes = self.g2p(text)
                if not phonemes:
                    log.warning("  G2P returned empty phonemes, using text directly.")
                    phonemes = text
                samples, sample_rate = self.kokoro.create(
                    phonemes,
                    voice=self.voice,
                    speed=self.speed,
                    is_phonemes=True,
                )
            else:
                # Direct mode: Japanese, Chinese, or fallback when misaki
                # is not installed. Pass lang_code so Kokoro picks the
                # correct internal G2P.
                samples, sample_rate = self.kokoro.create(
                    text,
                    voice=self.voice,
                    speed=self.speed,
                    lang=self.lang_code,
                )

            # Save audio
            sf.write(output_path, samples, sample_rate)

            # Calculate exact duration from samples
            duration = len(samples) / sample_rate
            log.info(
                f"  Audio generated: {duration:.2f}s "
                f"({len(samples)} samples @ {sample_rate}Hz)"
            )
            return duration

        except Exception as e:
            raise RuntimeError(f"Error generating audio with Kokoro: {e}")


